// ##########################
// basic content render function
// but render backgroundcolor 2 single color
// only ascill character
// ##########################
// ##########################
void wldlcd_draw_string_minium(uint16_t x, uint16_t y, char *string,
                               uint32_t color, uint16_t backgroundcolor) {
  uint8_t *buffer_point = wldlcd_buffer;
  uint8_t character_counter = 0;
  while (*string > 31 & *string < 127) {
    uint8_t row = 0;
    uint8_t column = 0;
    unsigned char tmp = 0;
    for (row = 0; row < 16; row++) {
      tmp = wldutil_ascill[*string - 32][row];
      for (column = 0; column < 8; column++) {
        if (tmp & 0x80) {
          *buffer_point = color >> 8;
          buffer_point++;
          *buffer_point = color;
          buffer_point++;
        } else {
          *buffer_point = backgroundcolor >> 8;
          buffer_point++;
          *buffer_point = backgroundcolor;
          buffer_point++;
        }
        tmp = tmp << 1;
      }
    }
    buffer_point = wldlcd_buffer;
    lcd_ili9341_drawimage(x + character_counter * 8, y, 8, 16, buffer_point);
    character_counter++;
    string++;
  }
}
