void wldlcd_draw_line_method_2(uint16_t x_start, uint16_t y_start,
                               uint16_t length, uint16_t width, uint16_t angle,
                               uint32_t color) {
  float x_vector, y_vector;
  if (angle <= 90) {
    x_vector = wldtool_cos_lookuptable[angle];
    y_vector = wldtool_sin_lookuptable[angle];
  } else {
    x_vector = wldtool_cos_lookuptable[180 - angle];
    y_vector = wldtool_sin_lookuptable[180 - angle];
  }
  uint16_t x_length = 0, y_length = 0;
  uint16_t x_width = 0, y_width = 0;
  uint16_t render_process = 0;
  x_width = width * y_vector;
  y_width = width * x_vector;
  x_length = length * x_vector;
  y_length = length * y_vector;
  if (angle <= 90) {
    if ((x_length + x_width) >= (y_length + y_width)) {
      // ========================== Configure ================================
      for (render_process = y_start - y_width;
           render_process <= y_start + y_length + y_width; render_process++) {
        if (render_process <= y_start + y_width) {
          wldlcd_draw_block(
              x_start - (render_process - y_start) / x_vector * y_vector,
              render_process,
              x_start + x_width +
                  (render_process - y_start) / y_vector * x_vector,
              render_process, color);

        } else if (render_process >= y_start + y_length) {
          wldlcd_draw_block(
              x_start - x_width +
                  (render_process - y_start - y_width) / y_vector * x_vector,
              render_process,
              x_start + x_length -
                  (render_process - y_start - y_length) / x_vector * y_vector,
              render_process, color);

        } else {
          wldlcd_draw_block(
              x_start - x_width +
                  (render_process - y_start - y_width) / y_vector * x_vector,
              render_process,
              x_start + x_width +
                  (render_process - y_start) / y_vector * x_vector,
              render_process, color);
        }
      }

    } else {
      // ========================== Configure ================================
      for (render_process = x_start - x_width;
           render_process <= x_start + x_length; render_process++) {
        if (render_process <= x_start) {
          wldlcd_draw_block(
              render_process,
              y_start + y_width -
                  (render_process - x_start + x_width) / y_vector * x_vector,
              render_process,
              y_start + y_width +
                  (render_process - x_start + x_width) / x_vector * y_vector,
              color);

        } else if (render_process >= x_start + x_length - x_width) {
          wldlcd_draw_block(
              render_process,
              y_start + (render_process - x_start) / x_vector * y_vector,
              render_process,
              y_start + y_width + y_length -
                  (render_process - x_start - x_length + x_width) / y_vector *
                      x_vector,
              color);

        } else {
          wldlcd_draw_block(
              render_process,
              y_start + (render_process - x_start) / x_vector * y_vector,
              render_process,
              y_start + y_width +
                  (render_process - x_start + x_width) / x_vector * y_vector,
              color);
        }
      }
    }
  } else {
    // ========================== Configure ================================
    if ((x_length + x_width) >= (y_length + y_width)) {
      for (render_process = y_start - y_width;
           render_process <= y_start + y_length + y_width; render_process++) {
        if (render_process <= y_start + y_width) {
          wldlcd_draw_block(
              x_start - (render_process - y_start) / y_vector * x_vector,
              render_process,
              x_start + (render_process - y_start) / x_vector * y_vector,
              render_process, color);

        } else if (render_process >= y_start + y_length) {
          wldlcd_draw_block(
              x_start - x_length +
                  (render_process - y_start - y_length) / x_vector * y_vector,
              render_process,
              x_start + x_width -
                  (render_process - y_start - y_width) / y_vector * x_vector,
              render_process, color);

        } else {
          wldlcd_draw_block(
              x_start - (render_process - y_start) / y_vector * x_vector,
              render_process,
              x_start + x_width -
                  (render_process - y_start - y_width) / y_vector * x_vector,
              render_process, color);
        }
      }
    } else {
      // ========================== Configure ================================
      for (render_process = x_start + x_width;
           render_process >= x_start - x_length; render_process--) {
        if (render_process >= x_start) {
          wldlcd_draw_block(
              render_process,
              y_start + y_width -
                  (x_start + x_width - render_process) / y_vector * x_vector,
              render_process,
              y_start + y_width +
                  (x_start + x_width - render_process) / x_vector * y_vector,
              color);

        } else if (render_process <= x_start - x_length + x_width) {
          wldlcd_draw_block(
              render_process,
              y_start + (x_start - render_process) / x_vector * y_vector,
              render_process,
              y_start + y_width + y_length -
                  (x_start - x_length + x_width - render_process) / y_vector *
                      x_vector,
              color);

        } else {
          wldlcd_draw_block(
              render_process,
              y_start + (x_start - render_process) / x_vector * y_vector,
              render_process,
              y_start + y_width +
                  (x_start + x_width - render_process) / x_vector * y_vector,
              color);
        }
      }
    }
  }
}
