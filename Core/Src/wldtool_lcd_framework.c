void wldlcd_draw_line_p2a(uint16_t x_start, uint16_t y_start, uint16_t length,
                          int16_t angle, uint32_t color, uint16_t width) {
  float x_vector = 0, y_vector = 0;
  angle = -angle;
  while (angle < 0)
    angle += 360;
  ;
  while (angle > 360)
    angle -= 360;
  ;
  if (angle % 90 <= 20 || angle % 90 >= 70) {
    wldlcd_draw_line_method_1(x_start, y_start, length, angle, color, width);
    return;
  }
  if (angle <= 90) {
    x_vector = wldtool_cos_lookuptable[angle];
    y_vector = wldtool_sin_lookuptable[angle];
    wldlcd_draw_line_method_2(x_start + width * y_vector / 2,
                              y_start - width * x_vector / 2, length, width,
                              angle, color);

  } else if (angle <= 180 & angle > 90) {
    x_vector = wldtool_cos_lookuptable[180 - angle];
    y_vector = wldtool_sin_lookuptable[180 - angle];
    wldlcd_draw_line_method_2(x_start - width * y_vector / 2,
                              y_start - width * x_vector / 2, length, width,
                              angle, color);

  } else if (angle <= 270 & angle > 180) {
    x_vector = wldtool_cos_lookuptable[angle - 180];
    y_vector = wldtool_sin_lookuptable[angle - 180];
    wldlcd_draw_line_method_2(
        x_start + width * y_vector / 2 - length * x_vector,
        y_start + width * x_vector / 2 - length * y_vector, length, width,
        angle - 180, color);

  } else {
    x_vector = wldtool_cos_lookuptable[360 - angle];
    y_vector = wldtool_sin_lookuptable[360 - angle];
    wldlcd_draw_line_method_2(
        x_start - width * y_vector / 2 + length * x_vector,
        y_start + width * x_vector / 2 - length * y_vector, length, width,
        angle - 180, color);
  }
}
