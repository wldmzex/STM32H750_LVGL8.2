// ##########################
//
// basic character render function
// only ascill character
// ##########################
// ##########################
void wldlcd_draw_single_character(uint16_t x, uint16_t y, char c,
                                  uint32_t color, uint32_t backgroundcolor,
                                  uint8_t fontsize) {
  uint8_t *buffer_point = wldlcd_buffer;
  uint8_t row = 0, column = 0, fontsize_row = fontsize,
          fontsize_column = fontsize;
  unsigned char tmp = 0;
  for (row = 0; row < 16; row++) {
    tmp = wldutil_ascill[c - 32][row];
    for (column = 0; column < 8; column++) {
      if (tmp & 0x80) {
        tmp = tmp << 1;
        wldlcd_draw_block(x + column * fontsize, y + row * fontsize,
                          x + column * fontsize + fontsize - 1,
                          y + row * fontsize + fontsize - 1, color);
      } else {
        tmp = tmp << 1;
        if (backgroundcolor == 0x80000000)
          continue;
        wldlcd_draw_block(x + column * fontsize, y + row * fontsize,
                          x + column * fontsize + fontsize - 1,
                          y + row * fontsize + fontsize - 1, backgroundcolor);
      }
    }
  }
}
// ##########################
// ##########################
// content render function
// only ascill character
//
// ##########################
// ##########################
void wldlcd_draw_string(uint16_t x, uint16_t y, char *string, uint32_t color,
                        uint32_t backgroundcolor, uint8_t fontsize) {
  uint8_t character_counter = 0;
  while (*string > 31 & *string < 127) {
    character_counter++;
    wldlcd_draw_single_character(x + 8 * fontsize * character_counter, y,
                                 *string, color, backgroundcolor, fontsize);
    string++;
  }
}
