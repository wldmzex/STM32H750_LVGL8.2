void wldlcd_draw_line_method_1(uint16_t x_start, uint16_t y_start,
                               uint16_t length, int16_t angle, uint32_t color,
                               uint16_t width) {

  uint16_t render_process = 0;
  float x_vector = 0, y_vector = 0;
  while (angle < 0)
    angle += 360;
  ;
  while (angle > 360)
    angle -= 360;
  ;
  if (angle <= 90) {
    x_vector = wldtool_cos_lookuptable[angle];
    y_vector = wldtool_sin_lookuptable[angle];
  } else if (angle <= 180 & angle > 90) {
    x_vector = -wldtool_cos_lookuptable[180 - angle];
    y_vector = wldtool_sin_lookuptable[180 - angle];

  } else if (angle <= 270 & angle > 180) {
    x_vector = -wldtool_cos_lookuptable[angle - 180];
    y_vector = -wldtool_sin_lookuptable[angle - 180];

  } else {
    x_vector = wldtool_cos_lookuptable[360 - angle];
    y_vector = -wldtool_sin_lookuptable[360 - angle];
  }
  int x_start_render = x_start;
  int y_start_render = y_start;
  int width_render = width;
  for (width_render = 0; width_render < width; width_render++) {
    if (width - 1) {
      if (angle <= 90) {
        x_start_render =
            x_start - (int)((width / 2.0 - (width_render)) * y_vector);
        y_start_render =
            y_start + (int)((width / 2.0 - (width_render)) * x_vector);
      } else if (angle <= 180 & angle > 90) {
        x_start_render =
            x_start + (int)((width / 2.0 - (width_render)) * y_vector);
        y_start_render =
            y_start - (int)((width / 2.0 - (width_render)) * x_vector);

      } else if (angle <= 270 & angle > 180) {
        x_start_render =
            x_start + (int)((width / 2.0 - (width_render)) * y_vector);
        y_start_render =
            y_start - (int)((width / 2.0 - (width_render)) * x_vector);

      } else {
        x_start_render =
            x_start + (int)((width / 2.0 - (width_render)) * y_vector);
        y_start_render =
            y_start - (int)((width / 2.0 - (width_render)) * x_vector);
      }
    }
    for (render_process = 0; render_process <= length; render_process++) {
      wldlcd_draw_point(x_start_render + render_process * x_vector,
                        y_start_render + render_process * y_vector, color);
    }
  }
}
